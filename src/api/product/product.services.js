// Service method to get all collection products

exports.getProductCollections = async (category, page, pageSize) => {
    try {

        let query = {};

        if (category !== 'all') {
            query = {
                category: { $in: [category] }, // Filter products by category
            };
        }

        const totalCount = await Product.countDocuments(query); // Get the total count of products

        if (totalCount === 0) {
            return {
                error: 'No products found for the provided category',
            };
        }

        const totalPages = Math.ceil(totalCount / pageSize); // Calculate the total number of pages

        if (page < 1 || page > totalPages) {
            return {
                error: 'Invalid page number',
            };
        }

        const skip = (page - 1) * pageSize; // Calculate the number of products to skip based on the page number
        let products;

        if (category === 'all') {
            products = await Product.find()
                .skip(skip)
                .limit(pageSize)
                .exec();
        } else {
            products = await Product.find(query)
                .skip(skip)
                .limit(pageSize)
                .exec();
        }

        return {
            products,
            totalPages,
        };
    } catch (error) {
        throw error;
    }
};

