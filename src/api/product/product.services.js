exports.searchProducts = async (searchQuery, page, pageSize) => {

    try {
        const searchText = searchQuery.toLowerCase(); // Convert search query to lowercase for case-insensitive matching

        const searchFilter = {
            $text: { $search: searchText } // Use MongoDB's text search feature
        };

        const projection = {
            score: { $meta: 'textScore' } // Include the text score in the result for sorting
        };

        const sortCriteria = {
            score: { $meta: 'textScore' } // Sort by text score (higher score indicates a better match)
        };

        const skip = (page - 1) * pageSize;

        const countPromise = Product.find(searchFilter).countDocuments();
        const productsPromise = Product.find(searchFilter, projection)
            .sort(sortCriteria)
            .skip(skip)
            .limit(pageSize)
            .exec();

        const [totalCount, products] = await Promise.all([countPromise, productsPromise]);

        const totalPages = Math.ceil(totalCount / pageSize);

        return {
            products,
            totalPages
        };
    } catch (error) {
        throw error;
    }
};